"""Demonstrate list indexing with a list of names."""

# Create and display a list of names.
names: list = ["Rayn", "Amari", "Asa", "Mathias", "Yosef"]
print(names)

# Display the first and last names.
print(names[0])
print(names[4])

# Change the second name.
names[1] = "Micah"

# Display the whole list.
print(names)

